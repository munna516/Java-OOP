package Encapsulation;

public class SetterGetter {
    private int id;
    private String name;

    public void setname(String name) {
        this.name = name;
    }

    public void setid(int id) {
        this.id = id;
    }

    public String getname() {
        return name; 
    }

    public int getid() {
        return id;
    }
}
