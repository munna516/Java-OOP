package Introduction;

public class Thiskeyword {
    int height, width, deepth;

    Thiskeyword(int height, int width, int deepth) {
        this.height = height;
        this.width = width;
        this.deepth = deepth;
    }

    void display() {
        System.out.println(height);
        System.out.println(width);
        System.out.println(deepth);
    }
}
